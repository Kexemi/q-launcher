import { chromium } from 'playwright';
import fs from 'node:fs';
import assert from 'node:assert/strict';

const token = fs.readFileSync('C:/Users/receg/AI-OS/System/console-token.txt','utf8').replace(/^\uFEFF/,'').trim();
const out = 'C:/Users/receg/AI-OS/Obsidian Vault/System/Automation/Logs/Owner-App-Recovery';
const browser = await chromium.launch({headless:true,executablePath:'C:/Program Files/Google/Chrome/Application/chrome.exe'});
const context = await browser.newContext({viewport:{width:390,height:665},deviceScaleFactor:2});
await context.addInitScript(t => localStorage.setItem('aios_token', t), token);
const page = await context.newPage();
let failApi = false;
let rotateEndpoint = false;
let rotationLookups = 0;
let expectedRotationFailures = 0;
const errors = [];
page.on('pageerror', e => errors.push('page:'+String(e)));
page.on('requestfailed', r => {
  if (rotateEndpoint && r.url().includes('127.0.0.1:8999')) expectedRotationFailures += 1;
  else if (!failApi) errors.push('request:'+r.url());
});
await page.route('**/url.json*', r => {
  rotationLookups += 1;
  const url = rotateEndpoint && rotationLookups === 1 ? 'http://127.0.0.1:8999' : 'http://127.0.0.1:8778';
  return r.fulfill({status:200,contentType:'application/json',body:JSON.stringify({url})});
});
await page.route('**/api/**', r => failApi ? r.abort('failed') : r.continue());
await page.goto('http://127.0.0.1:8765/app/#world',{waitUntil:'networkidle'});
await page.waitForFunction(()=>document.querySelector('#world-progress')?.textContent==='11/11');
assert.equal(new URL(page.url()).origin,'http://127.0.0.1:8765');
assert.equal(await page.locator('#connection').textContent(),'connected');
await page.screenshot({path:out+'/real-app-world-phone.png'});
for (let i=0;i<10;i++) {
  for (const view of ['decisions','focus','world']) await page.locator(`nav button[data-go="${view}"]`).click();
}
assert.equal(new URL(page.url()).origin,'http://127.0.0.1:8765');
await page.locator('nav button[data-go="decisions"]').click();
await page.waitForFunction(()=>document.querySelector('#decision-stage')?.textContent.includes('11/11 complete'));
await page.locator('nav button[data-go="focus"]').click();
await page.goBack();
assert.match(page.url(),/#decisions$/);
await page.goForward();
assert.match(page.url(),/#focus$/);
await page.locator('nav button[data-go="world"]').click();
await page.locator('#primary-region').click();
assert.match(page.url(),/#focus$/);
await page.screenshot({path:out+'/real-app-focus-phone.png'});
failApi = true;
await page.locator('nav button[data-go="decisions"]').click();
await page.waitForFunction(()=>document.querySelector('#decision-stage')?.textContent.includes('Still reconnecting'),null,{timeout:20000});
assert.equal(new URL(page.url()).origin,'http://127.0.0.1:8765');
assert.match(page.url(),/#decisions$/);
failApi = false;
await page.locator('#dec-retry').click();
await page.waitForFunction(()=>document.querySelector('#decision-stage')?.textContent.includes('11/11 complete'));
rotateEndpoint = true;
rotationLookups = 0;
await page.evaluate(()=>{ S.base = ''; });
await page.locator('nav button[data-go="world"]').click();
await page.locator('nav button[data-go="decisions"]').click();
await page.waitForFunction(()=>document.querySelector('#decision-stage')?.textContent.includes('11/11 complete'),null,{timeout:20000});
assert.ok(rotationLookups >= 2, 'stale endpoint must cause fresh url.json discovery');
assert.ok(expectedRotationFailures >= 1, 'test must exercise the stale endpoint');
assert.equal(new URL(page.url()).origin,'http://127.0.0.1:8765');
await page.screenshot({path:out+'/real-app-decisions-phone.png'});
assert.equal(await page.evaluate(()=>document.documentElement.scrollWidth>document.documentElement.clientWidth),false);
assert.deepEqual(errors,[]);
console.log(JSON.stringify({pass:true,origin:new URL(page.url()).origin,progress:await page.locator('#world-progress').textContent(),view:'decisions',disconnect_recovered:true,tunnel_rotation_recovered:true,rotation_lookups:rotationLookups,expected_rotation_failures:expectedRotationFailures,rapid_taps:30,history:true,overflow:false,errors},null,2));
await browser.close();
